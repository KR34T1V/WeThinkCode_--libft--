#include "../inc/libft.h"
#include "../inc/private_libft_includes.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!n)
		return (1);
	if (s1 && s2 && n)
	{
		if (ft_strncmp(s1, s2, n) == 0)
			return (1);
		else
			return (0);
	}
	return (0);
}
