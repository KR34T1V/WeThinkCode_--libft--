#include "../inc/libft.h"
#include "../inc/private_libft_includes.h"

/*
	Copies "n" bytes of memory form "src" to "dst",
	and returns newly populated dst pointer.
*/

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	if (n == 0)
		return (dst);
	while (n--)
		*d++ = *s++;
	return (dst);
}
